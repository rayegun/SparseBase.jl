# Useful Operators not found elsewhere:
#######################################
rowindex(x) = 1::Int # row index of element x
colindex(x) = 1::Int # col index of element x

firsti(x, y) = 1::Int # row index of element x
secondi(x, y) = 1::Int # row index of element y
firstj(x, y) = 1::Int # col index of element x
secondj(x, y) = 1::Int # col index of element y

second(x, y) = y # analogous to first.
